    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String S1 = jTextField1.getText();
       String S2 = jTextField6.getText();
       String S3 = jTextField5.getText();
       String S4 = jTextField3.getText();
       String S5 = jTextField4.getText();
        int count = 0;
        if(S1 == "")
        {
            JOptionPane.showMessageDialog(this,"Enter the Movie Name");
            count =0;
        }
        for(int i=0;i<A.Movie_Name.size();i++)
        {
        if(A.Movie_Name.get(i).equals(S1))
        {
            A.Movie_Name.remove(i);
            A.Movie_Language.remove(i);
            A.Movie_Genre.remove(i);
            A.Movie_Duration.remove(i);
            A.Movie_Cast.remove(i);
            count = 1;
            break;
        }
        }
        if(count == 1)
        {
            A.Movie_Name.add(S1);
            A.Movie_Language.add(S2);
            A.Movie_Genre.add(S3);
            A.Movie_Duration.add(S4);
            A.Movie_Cast.add(S5);
            JOptionPane.showMessageDialog(this,"The movie is Updated");
            new Admin().setVisible(true);
            dispose();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

